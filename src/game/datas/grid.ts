type Atlas = { [key: number]: (x: number, y: number) => Actor }

const atlas: Atlas = {
  1: wall,
}

export function drawGrid(grid: number[][], scene: Scene) {
  for (let y = 0; y < grid.length; y++) {
    for (let x = 0; x < grid[0].length; x++) {
      const key = grid[y][x]
      const actor = atlas[key]
      if (actor) {
        scene.add(actor(x, y))
      }
    }
  }
}
